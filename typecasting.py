# typecasting.py
# typecasting - converting one data type to another data type
# int() - converts to integer
# float() - converts to float
# str() - converts to string
# list() - converts to list
# tuple() - converts to tuple
# dict() - converts to dictionary
# set() - converts to set
# complex() - converts to complex number
# bool() - converts to boolean
# chr() - converts to character
# ord() - converts to ASCII value
# hex() - converts to hexadecimal
# oct() - converts to octal
# bin() - converts to binary
# eval() - evaluates a string as a Python expression

# first program in python
name = "sally"
age = 25
height = 7.99
is_student = True

print("name is", type(name))
print(type(age))
print(type(height))
print(type(is_student))

# typecasting
age = float(age)

print("age is", type(age))

print(age)

height = int(height)
print(height)
